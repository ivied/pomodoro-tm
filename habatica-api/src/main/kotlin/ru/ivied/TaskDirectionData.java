package ru.ivied;

public class TaskDirectionData {
    private float delta;
    public Double exp;
    public Double hp;
    public Double gp;
    public Double mp;
    public Integer lvl;

    public float getDelta() {
        return delta;
    }

    public void setDelta(float delta) {
        this.delta = delta;
    }

    public Double getExp() {
        return exp;
    }

    public Double getHp() {
        return hp;
    }

    public Double getGp() {
        return gp;
    }

    public Double getMp() {
        return mp;
    }

    public Integer getLvl() {
        return lvl;
    }
}
